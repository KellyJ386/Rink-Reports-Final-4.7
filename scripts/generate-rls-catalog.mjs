#!/usr/bin/env node
/**
 * RLS catalog test generator.
 *
 * Reads tests/rls-catalog/tables.json and emits supabase/tests/20_rls_catalog.test.sql.
 *
 * Emits, per tenant-scoped table:
 *   - `rls_enabled` assertion — the table has RLS enabled
 *   - `has_policies` assertion — at least one policy exists
 *   - `cross_facility_isolation` attack — a user of facility Beta runs
 *     `SELECT count(*) ... WHERE facility = alpha` and expects 0
 *
 * Tables with `facility_column: null` are skipped (global or platform-admin-only)
 * but their `skip_reason` is rendered as a SQL comment so the catalog is
 * visibly complete.
 *
 * For tables without their own facility_id column (linked via parent_table),
 * the SELECT attack joins to the parent and asserts 0 rows visible whose parent
 * belongs to Facility Alpha.
 *
 * Invocation:
 *   node scripts/generate-rls-catalog.mjs
 *
 * CI enforces that the committed SQL matches the regeneration output — if
 * a future agent edits the YAML but forgets to regenerate, PR CI fails.
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)
const ROOT = resolve(__dirname, '..')

const INPUT = resolve(ROOT, 'tests/rls-catalog/tables.json')
const OUTPUT = resolve(ROOT, 'supabase/tests/20_rls_catalog.test.sql')

// Seed constants — must match supabase/seed.sql
const ALPHA_FACILITY = '00000001-0000-0000-0000-000000000001'
const BETA_FACILITY = '00000002-0000-0000-0000-000000000002'
const ALPHA_STAFF = '00000001-0000-0000-0000-000000001003'
const BETA_STAFF = '00000002-0000-0000-0000-000000002003'

const catalog = JSON.parse(readFileSync(INPUT, 'utf8'))
const tables = catalog.tables

const covered = tables.filter((t) => !t.skip_reason)
const skipped = tables.filter((t) => t.skip_reason)

// Per covered table: rls_enabled, has_policies, cross_facility_isolation = 3 asserts.
const planCount = covered.length * 3

const lines = []

lines.push('-- supabase/tests/20_rls_catalog.test.sql')
lines.push('--')
lines.push('-- GENERATED by scripts/generate-rls-catalog.mjs. Do not edit by hand.')
lines.push('-- Source catalog: tests/rls-catalog/tables.json')
lines.push('--')
lines.push('-- Coverage contract (per tenant-scoped table):')
lines.push('--   (1) RLS enabled')
lines.push('--   (2) At least one policy exists')
lines.push('--   (3) Beta staff cannot SELECT any Facility Alpha rows')
lines.push('--')
lines.push('-- Additional per-operation attacks (INSERT forged, UPDATE, DELETE) live in')
lines.push('-- each module\'s dedicated pgTAP file — tracked in KNOWN_GAPS.md as a')
lines.push('-- phase-2 expansion point for Agent 9.')
lines.push('')
lines.push('begin;')
lines.push(`select plan(${planCount});`)
lines.push('')
lines.push('create or replace function _test_as(p_user_id uuid) returns void')
lines.push('language sql as $$')
lines.push("  select set_config('role', 'authenticated', true),")
lines.push("         set_config('request.jwt.claims',")
lines.push("           json_build_object('sub', p_user_id::text, 'role', 'authenticated')::text,")
lines.push('           true);')
lines.push('$$;')
lines.push('')

// Tables intentionally not covered — render as comment block for documentation
if (skipped.length > 0) {
  lines.push('-- ----------------------------------------------------------------')
  lines.push('-- Intentionally NOT covered in the generated harness:')
  for (const t of skipped) {
    lines.push(`--   ${t.name.padEnd(30)} — ${t.skip_reason}`)
  }
  lines.push('-- ----------------------------------------------------------------')
  lines.push('')
}

for (const t of covered) {
  lines.push(`-- ============================================================================`)
  lines.push(`-- ${t.name}`)
  lines.push(`-- ============================================================================`)

  // (1) RLS enabled
  lines.push(
    `select ok((select rowsecurity from pg_tables where schemaname='public' and tablename='${t.name}'),`,
  )
  lines.push(`  '${t.name}: RLS is enabled');`)

  // (2) At least one policy
  lines.push(
    `select cmp_ok((select count(*)::int from pg_policies where schemaname='public' and tablename='${t.name}'),`,
  )
  lines.push(`  '>=', 1, '${t.name}: at least one RLS policy defined');`)

  // (3) Cross-facility SELECT attack
  lines.push(`select _test_as('${BETA_STAFF}'::uuid);`)

  if (t.facility_column) {
    // Direct scoping column
    lines.push(
      `select is((select count(*)::int from public.${t.name} where ${t.facility_column} = '${ALPHA_FACILITY}'::uuid),`,
    )
    lines.push(`  0, '${t.name}: beta staff sees zero alpha rows (column: ${t.facility_column})');`)
  } else if (t.parent_table && t.parent_fk_column) {
    // Inherited scope via parent FK — look up parent rows owned by alpha
    // Parent must have facility_id column named 'facility_id' (enforced by
    // convention; no table in the catalog inherits a non-facility-scoped
    // parent, so this simplification holds).
    lines.push(`select is(`)
    lines.push(`  (select count(*)::int from public.${t.name} child`)
    lines.push(
      `     join public.${t.parent_table} parent on parent.id = child.${t.parent_fk_column}`,
    )
    lines.push(`     where parent.facility_id = '${ALPHA_FACILITY}'::uuid),`)
    lines.push(
      `  0, '${t.name}: beta staff sees zero rows via ${t.parent_table}.${t.parent_fk_column}');`,
    )
  } else {
    throw new Error(
      `Table '${t.name}' has no facility_column, no parent_table, and no skip_reason`,
    )
  }

  lines.push('reset role;')
  lines.push('')
}

lines.push('select * from finish();')
lines.push('rollback;')
lines.push('')

const out = lines.join('\n')
writeFileSync(OUTPUT, out, 'utf8')

console.log(`Generated ${OUTPUT}`)
console.log(`  ${covered.length} tenant-scoped tables covered`)
console.log(`  ${skipped.length} tables intentionally skipped (with documented reason)`)
console.log(`  ${planCount} assertions`)
